function createHours(weekdayFrom, weekdayTo, saturdayFrom = "10:00", saturdayTo = "15:00") {
  return {
    mon: [{ from: weekdayFrom, to: weekdayTo }],
    tue: [{ from: weekdayFrom, to: weekdayTo }],
    wed: [{ from: weekdayFrom, to: weekdayTo }],
    thu: [{ from: weekdayFrom, to: weekdayTo }],
    fri: [{ from: weekdayFrom, to: weekdayTo }],
    sat: saturdayFrom && saturdayTo ? [{ from: saturdayFrom, to: saturdayTo }] : [],
    sun: [],
  };
}

